class Sort
{
    public void sort1()
    {
        int ar[] = { 2,3,4,9,5,7,6};
        boolean  change = true;  // to check if it is their then swap the elements
        int arlen = ar.length;   //this array length 
        while( change)
        {
            change = false;
            for(int index= 0; index < arlen-1; index++)
            {
                if(ar[index] < ar[index + 1])
                {
                    int temp = ar[index];
                    ar[index] = ar[index+1];  // This is swap Because comparing Two numbers if first number small then swap to second number
                    ar[index+1] = temp;
                    change = true;
                }
            }
        }
        for(int num  : ar )
        {
            System.out.print(num + " ");
        }
    } 
}





